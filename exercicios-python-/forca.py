import random


def jogar():

    imprime_mensagem_inicial()
    palavra_secreta = carrega_palavra_secreta()
    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)
    print(letras_acertadas)

    enforcou = False
    acertou = False
    erros = 0

    while(not enforcou and not acertou):

        chute = obter_chute()

        if chute in palavra_secreta:
            marcar_letras_acertadas(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if acertou:
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)

    print("Fim do jogo")


def imprime_mensagem_inicial():
    print("*********************************")
    print("***Bem vindo ao jogo de Forca!***")
    print("*********************************")

def carrega_palavra_secreta():
    #arquivo = open("palavras.txt", "r") - modo alternativo para abertura de arquivo
    palavras = []
    with open("palavras.txt") as arquivo:
        for linha in arquivo:
            linha = linha.strip()
            palavras.append(linha)

    #arquivo.close() - modo alternativo para fechamento do aquivo
    #O modo implementado para abertura do arquivi permite q o python feche o mesmo automaticamente em caso de erro

    return random.choice(palavras).upper()

def inicializa_letras_acertadas(palavra):
    return ["_"] * len(palavra)
    #alternativamente: ["_" for letra in palavra]

def obter_chute():
    chute = input("Qual a letra? ")
    chute = chute.strip().upper()
    return chute

def marcar_letras_acertadas(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if chute == letra:
            letras_acertadas[index] = letra
        index += 1

